#  Math Function
import math

pi = 3.14
minpi = -3.14
x = 1
y = 2
z = 3

print(round(pi))            # Membulatkan bilangan secara netral
print(math.ceil(pi))        # Selalu membulatkan bilangan menjadi lebih besar
print(math.floor(pi))       # Selalu membulatkan bilangan menjadi lebih kecil
print(abs(minpi))           # Menampilkan nilai absolute
print(math.pow(pi,2))       # Operasi menaikkan bilangan dasar menjadi pangkat
print(math.sqrt(49))        # Operasi akar bilangan
print(max(x, y, z))         # Mencari nilai terbesar
print(min(x, y, z))         # Mencari nilai terkecil